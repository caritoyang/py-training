# Index
emailAddress='carolina.yang@gmail.com'

# Fetch value from any index, just by passing index
print(emailAddress[13])

# Fetch substring by start and end index
print(emailAddress[0:3])

# Fetch substring by giving start index only
print(emailAddress[9:])

# Fetch substring by giving end index only
print(emailAddress[:4])

# Fetch from end to start
print(emailAddress[::-1])